package io.github.vatisteve.utils.excel.common;

/**
 * Represents the different types of elements found in an Excel workbook.
 * This enumeration defines key structural components of an Excel file.
 */
public enum ExcelElement {
    /**
     * Represents a sheet in an Excel workbook.
     */
    SHEET,
    /**
     * Represents a row in an Excel sheet.
     */
    ROW,
    /**
     * Represents a column in an Excel sheet.
     */
    COLUMN,
    /**
     * Represents a cell in an Excel sheet.
     */
    CELL
}
