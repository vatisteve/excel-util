package io.github.vatisteve.utils.excel.common;

/**
 * Represents the types of identifiers that can be used to reference elements.
 * This enumeration defines the possible ways elements can be identified, such as by their name or position.
 */
public enum ElementIdentifier {

    /**
     * The element is identified by its name.
     */
    NAME,

    /**
     * The element is identified by its position in the workbook.
     */
    POSITION;

}
